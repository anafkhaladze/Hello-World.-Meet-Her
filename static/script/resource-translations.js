function setLanguage(lang) {
  const data = resourcesTranslations[lang];

  // Example: Set Mental Health title & description
  document.querySelector("#resources h3").textContent = data.mentalHealth.title;
  document.querySelector("#resources p").textContent = data.mentalHealth.description;

  // Update list items dynamically (simplified example)
  const ul = document.querySelector("#resources ul");
  ul.innerHTML = ""; // clear

  data.mentalHealth.list.forEach(item => {
    const li = document.createElement("li");

    const a = document.createElement("a");
    a.href = item.url;
    a.textContent = item.name;
    li.appendChild(a);

    const p = document.createElement("p");
    p.textContent = item.whyGreat;
    li.appendChild(p);

    ul.appendChild(li);
  });
}
